import path from 'path';
import { defineConfig, loadEnv } from 'vite';
import react from '@vitejs/plugin-react';

export default defineConfig(({ mode }) => {
    const env = loadEnv(mode, '.', '');
    return {
      server: {
        port: 3000,
        host: '0.0.0.0',
      },
      plugins: [react()],
      define: {
        'process.env.API_KEY': JSON.stringify(env.GEMINI_API_KEY),
        'process.env.GEMINI_API_KEY': JSON.stringify(env.GEMINI_API_KEY)
      },
      resolve: {
        alias: {
          // FIX: Replaced `__dirname` with `'./'` to fix "Cannot find name '__dirname'" error.
          // In modern module systems (like ESM), `__dirname` is not available.
          // `path.resolve('./')` resolves to the current working directory, which is the project root
          // when running Vite, effectively achieving the same as the original code.
          '@': path.resolve('./'),
        }
      }
    };
});